/** Categoría por defecto cuando la IA no puede clasificar */
export const DEFAULT_CATEGORY = 'Nota';

/** Obtiene el objeto completo de una categoría por su value */
export function getCategory(value) {
  return CATEGORIES.find(c => c.value === value);
}

/** Obtiene el color de una categoría por su value */
export function getCategoryColor(value) {
  return getCategory(value)?.color || 'var(--on-surface-variant)';
}

/** Obtiene el label de una categoría por su value */
export function getCategoryLabel(value) {
  return getCategory(value)?.label || value;
}

/** Obtiene el ícono SVG de una categoría por su value */
export function getCategoryIcon(value) {
  return getCategory(value)?.icon || CATEGORIES[4].icon; // Nota como fallback
}
